#!/usr/bin/env python3


# PYTHON TRAINING
# In Python there are three distinct numeric types:
# Integers
# Floating-point numbers
# Complex numbers

##### INTEGERS #####

x = 10
y = -10
z = 123456789

print(x, y, z)

##### BINARY #####

print(0b1011)

##### OCTAL #####

print(0o10)

##### HEX #####

print(0xFF)

##### Boolean is a subtype of Integers #####

x = True
y = False
